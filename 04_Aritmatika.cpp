#include <iostream>
using namespace std;

int main(){
    int x, y, z;

    x = 10;
    y = 5;
    z = 3;

    cout << "Nilai Awal x : " << x << endl;
    cout << "Nilai Awal y : " << y << endl;
    cout << "Nilai Awal z : " << z << endl;
    cout << endl;

    // Operasi 
    cout << "x + y = " << x + y << endl; // Tambah
    cout << "x - y = " << x - y << endl; // Pengurangan
    cout << "x * y = " << x * y << endl; // Perkalian
    cout << "x / y = " << x / y << endl; // Perbagian
    cout << "x % z = " << x % z << endl; // modulus

    // Dah :v

    return 0;
}